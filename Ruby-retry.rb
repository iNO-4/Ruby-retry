puts 'Hello, World!!'

# 「puts」は、「受け取った値を文字列として出力」する働き
# このように何かしらの処理をさせる命令を、Rubyでは「メソッド」と呼ぶ
# また、その後ろに続く記述(Hello, World)を「引数」と呼ぶ
# 「引数」では「メソッド」に渡す値を記述する
# putsメソッドの「引数」'Hello, World'によって、「Hello, World」という文字列が出力される

# 文字列は（""）、（''）で囲う

# ターミナルで、「ruby 実行するプログラム名」 で実行できる
# 例：「ruby Ruby-retry.rb」

# Rubyでは基本的に１つの処理を行うプログラムを１行に記述する
# プログラムを記述する場合は処理毎に改行を行い、どこからどこまでが１つの処理なのかを明確にする
